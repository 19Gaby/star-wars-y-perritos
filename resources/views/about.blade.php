                    <h3>Su exito</h3>
                    <p>Su éxito ha sido lo bastante grande como para permitir a Lucas crear su propia productora (Lucasfilm) y una serie de empresas relacionadas (LucasArts, Industrial Light & Magic, etc.). Fueron las primeras películas que hicieron uso intensivo del merchandising como fuente de beneficios, extendiéndose rápidamente fuera de lo cinematográfico. Nada más salir la primera película, existían camisetas, pósters, tazas, cómics, libros, etc... basadas en los personajes de la película, y han seguido produciéndose hasta hoy, a pesar del parón de casi 20 años entre "El Regreso del Jedi" y "La Amenaza Fantasma".</p>
                    <p>Cuando Ronald Reagan presentó su Iniciativa de Defensa Estratégica (que incluía el lanzamiento de satélites capaces de destruir misiles en pleno vuelo), la prensa la apodó "Star Wars" ("La guerra de las galaxias"), por lo ambicioso del proyecto, que nunca llegó a realizarse, aunque actualmente se ha planteado un proyecto de escudo antimisiles, que es similar pero menos ambicioso.</p>
