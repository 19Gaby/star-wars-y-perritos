                         <h3>Star Wars: Episodio V - El Imperio contraataca</h3>
                         <p>Star Wars: Episodio V - El Imperio contraataca (título original en inglés: Star Wars: Episode V - The Empire Strikes Back; también conocida en español como La guerra de las galaxias: Episodio V - El Imperio contraataca) es una película del género space opera dirigida por Irvin Kershner y estrenada por primera vez en Estados Unidos el 21 de mayo de 1980. El guion, basado en una historia de George Lucas, fue escrito por Lawrence Kasdan y Leigh Brackett. Aunque en términos cronológicos internos sea la quinta película de la saga Star Wars, en realidad fue la segunda película de la saga en ser estrenada.</p>
