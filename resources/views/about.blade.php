                    <h2>Sobre la saga</h2>
                         <p>Star Wars, conocida también en español como La guerra de las galaxias, ​​​ es una franquicia compuesta primordialmente de una serie de películas concebidas por el cineasta estadounidense George Lucas en la década de 1970, y producidas y distribuidas por The Walt Disney Company a partir de 2012.</p>
                    <div class="col-md-4 col-sm-4">
                         <h2>Origen</h2>
                         <p>A pesar de que la mayoría de la gente considera a Star Wars como ciencia ficción, George Lucas ha declarado en numerosas ocasiones que estas películas no son de "ciencia ficción", principalmente porque la tecnología que muestra no tiene base científica. Así, serían de "aventura épica" y nada más.</p>
                         <p>Star Wars empezó en 1977, con el estreno de la primera entrega, con el título en castellano de La Guerra de las Galaxias (Star Wars, en inglés). A pesar de los problemas que tuvo Lucas para financiar la película, y de que apenas se esperaba recuperar la inversión realizada, fue un tremendo éxito, especialmente si se suman las ventas de artículos relacionados. Tres años después aparece su segunda parte (El Imperio Contraataca) y, finalmente, la tercera parte (El Regreso del Jedi), que en aquel momento George Lucas consideró como el último filme que haría de la saga.</p>
                         
                    </div>

                    <div class="col-md-4 col-sm-4">
                         <h2>Sobre la pelicula</h2>
                         <p>El argumento de las películas se sitúa "hace mucho tiempo, en una galaxia muy, muy lejana..." (de la nuestra) (ésta frase que aparece habitualmente al principio de cualquier historia relacionada). Allí se ha dado durante siglos un enfrentamiento entre el "lado luminoso de la Fuerza" (representado por los Jedi, protectores de la galaxia) y el "lado oscuro" (representado por los Sith).</p>
                         <p>En conclusión esta secuela nos cuenta cómo la República, que está formada por la mayoría de los sistemas planetarios de la galaxia, se enfrenta a una terrible guerra, en parte por los manejos de Darth Sidious (un misterioso maestro Sith, que es parte del lado oscuro de la fuerza). Narran el aprendizaje de un joven Jedi (Caballero del lado luminoso) de Anakin Skywalker, y su perversión al lado oscuro , ya que este cae por tentaciones fuertes para el ser humano, en este caso los sentimientos humanos.</p>
                    </div>

                    <div class="col-md-6 col-sm-6">
                         <h3>Extension de su saga</h3>
                         <p>Tras la trilogía original, el universo de ficción de Star Wars siguió creciendo en otros medios, principalmente a través de novelas y cómics, cuyas historias eran por lo general compatibles, formando una cronología que partía desde miles de años antes de las películas hasta los descendientes de los protagonistas de ellas. A este "universo" que extendía el original "oficial" de las películas se le llama "Universo Expandido".</p>
                         
