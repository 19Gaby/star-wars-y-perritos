                         <h3>Star Wars: Episodio VI - El retorno del Jedi</h3>
                         <p>Star Wars: Episode VI - Return of the Jedi (conocida en español como Star Wars: Episodio VI - El retorno del Jedi o La guerra de las galaxias: Episodio VI - El regreso del Jedi) es una película de space opera estrenada el 25 de mayo de 1983 (seis años después del estreno de la primera película) y dirigida por Richard Marquand. Fue la tercera película estrenada de la saga Star Wars y la sexta en términos de cronología interna de la saga. De la trilogía original fue la película más criticada, ya que mucha gente la consideraba la más infantil por la inclusión de los ewoks. Aun así fue aclamada por la mayoría de la gente, que la consideraron una gran conclusión para la saga.</p>
